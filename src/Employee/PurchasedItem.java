    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
       
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
         int sr_no = 0;
         int total_bill = 0;
         for(Map.Entry me: hm.entrySet())
         {
               sr_no = sr_no +1;
               String item_id =(String) me.getKey();
               ArrayList al = (ArrayList)me.getValue();
               
              String  item_name = (String) al.get(0);
               String item_price = (String)al.get(1);
               String item_quant = (String)al.get(2);
               
               int item_price1 = Integer.parseInt(item_price);
               int item_quantity = Integer.parseInt(item_quant);
               int total_rs = item_price1 * item_quantity;
               
               total_bill += total_rs; 
               
               Object[] row ={sr_no,item_id,item_name,item_price,item_quant,total_rs};
               dtm.addRow(row);
         }
         jTextField3.setText(String.valueOf(total_bill));
    }//GEN-LAST:event_formWindowOpened

