    private void jLabel8MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel8MouseClicked
        //----------------------------- Update ----------------------
        String id =jTextField1.getText();
        String quantity = (String) jComboBox1.getSelectedItem();
        
       ArrayList al = hm.get(id);
       al.set(2, quantity);
       // replace quantity in hashmap
       hm.replace(id,al);
       JOptionPane.showMessageDialog(rootPane,al.get(0)+ " Updated Successfully");  // at 0 the index there is a item_name in arrayList.
       new PurchasedItem(rme, hm).setVisible(true);
       setVisible(false);
    }//GEN-LAST:event_jLabel8MouseClicked

